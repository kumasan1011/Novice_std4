{	
	switch(piece)
    {
		case SFU:
		if(To<0x40){
			return 1;
		}
		break;
		case SKY: case SKE:
		if(To<0x30){
			return 1;
		}
		break;
		case EFU:
		if(To>0x70){
			return 1;
		}
		break;
		case EKY: case EKE:
		if(To>0x80){
			return 1;
		}
		break;
		case SKA: case SHI:
		case EKA: case EHI:
		return 1;
		break;
	}
	return 0;
}

